    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {

            if (jTable2.getRowCount() == 0) {
                throw new UnsupportedOperationException("Não existem dados na tabela a serem alterados.");

            }

            if ("".equals(jTxtNomeDebito_u.getText()) && "".equals(jTxtNomeCredito_u.getText()) && "".equals(jTxtNomeHistorico.getText())) {
                throw new UnsupportedOperationException("Você deve informar algum dado para alterar.");
            }

            if (cnfb.conecta()) {
                try {

                    boolean selected;
                    String numero;

                    if (jTable2.getSelectedRowCount() > 0) {

                        selected = true;

                    } else {
                        selected = false;
                    }

                    for (int i = 0; i < jTable2.getRowCount(); i++) {

                        numero = jTable2.getValueAt(i, 0).toString();

                        if (selected) {

                            if (jTable2.isRowSelected(i)) {
                                gravaDados(numero, jTxtCtaDebito_u.getText(), jTxtCtaCredito_u.getText(), jTxtHistorico.getText());
                            }

                        } else {
                            gravaDados(numero, jTxtCtaDebito_u.getText(), jTxtCtaCredito_u.getText(), jTxtHistorico.getText());
                        }

                    }

                } catch (Exception e) {
                    throw new UnsupportedOperationException(e);
                } finally {
                    cnfb.desconecta();
                    var_consulta = LANCAMENTOS;
                    incluiPesquisa();
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } finally {

        }
    }//GEN-LAST:event_jButton2ActionPerformed

