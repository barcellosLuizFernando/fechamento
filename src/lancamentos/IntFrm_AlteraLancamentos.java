    private void incluiPesquisa() {

        int linha = jTable2.getSelectedRow();

        switch (var_consulta) {
            case CTA_CREDITO_P:

                jTxtNomeCredito.setText(consultaContas(jTxtCtaCredito.getText()));

                break;

            case CTA_CREDITO_U:

                jTxtNomeCredito_u.setText(consultaContas(jTxtCtaCredito_u.getText()));

                break;

            case CTA_DEBITO_P:

                jTxtNomeDebito.setText(consultaContas(jTxtCtaDebito.getText()));

                break;

            case CTA_DEBITO_U:

                jTxtNomeDebito_u.setText(consultaContas(jTxtCtaDebito_u.getText()));

                break;

            case HISTORICO:

                //jTxtNomeHistorico.setText(consultaContas(jTxtHistorico.getText()));
                sql = "SELECT nome FROM CAD_HIST_CONTABIL "
                        + "where codigo = '" + jTxtHistorico.getText() + "' "
                        + "and empresa = 1;";

                if (cnfb.conecta()) {
                    try {
                        cnfb.executeConsulta(sql);
                        while (cnfb.rs.next()) {
                            jTxtNomeHistorico.setText(cnfb.rs.getString("nome"));
                        }
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, e.getMessage());
                    } finally {
                        cnfb.desconecta();
                    }
                }

                break;

            case LANCAMENTOS:

                DefaultTableModel lista = (DefaultTableModel) jTable2.getModel();

                lista.setRowCount(0);
                lista.setColumnCount(0);

                lista.addColumn("Número");
                lista.addColumn("Data");
                lista.addColumn("Débito");
                lista.addColumn("Crédito");
                lista.addColumn("Valor");
                lista.addColumn("Histórico");
                lista.addColumn("Complemento");

                try {
                    String dti = jTxtDti.getText().replace("/", ".");
                    String dtf = jTxtDtf.getText().replace("/", ".");

                    if ("  /  /    ".equals(dti) | "  /  /    ".equals(dtf)) {
                        jTxtDti.grabFocus();
                        throw new UnsupportedOperationException("Você deve informar data inicial e final para pesquisar.");
                    }

                    if ("".equals(jTxtNomeDebito.getText()) && "".equals(jTxtNomeCredito.getText())) {
                        jTxtCtaDebito.grabFocus();
                        throw new UnsupportedOperationException("Você deve selecionar ao menos uma conta para pesquisar.");
                    }

                    sql = "select a.numero, a.data, a.debito, a.credito, a.valor, "
                            + "a.historico, a.complemento "
                            + "from ctb_lancamentos a "
                            + "where a.empresa = 1 "
                            + "AND a.data between '" + dti + "' and '" + dtf + "' ";

                    if (!"".equals(jTxtNomeDebito.getText())) {
                        sql += "AND a.debito = '" + jTxtCtaDebito.getText() + "' ";
                    }

                    if (!"".equals(jTxtNomeCredito.getText())) {
                        sql += "AND a.credito = '" + jTxtCtaCredito.getText() + "' ";
                    }

                    if (!"".equals(jTxtComplemento.getText())) {
                        sql += "AND upper(a.complemento) like ('%" + jTxtComplemento.getText().toUpperCase() + "%') ";
                    }

                    if (cnfb.conecta()) {
                        try {
                            cnfb.executeConsulta(sql);
                            while (cnfb.rs.next()) {
                                lista.addRow(new String[]{
                                    cnfb.rs.getString("numero"),
                                    dateIn.format(cnfb.rs.getDate("data")),
                                    cnfb.rs.getString("debito"),
                                    cnfb.rs.getString("credito"),
                                    df.format(cnfb.rs.getDouble("valor")),
                                    cnfb.rs.getString("historico"),
                                    cnfb.rs.getString("complemento")
                                });
                            }
                        } catch (Exception e) {
                            throw new UnsupportedOperationException(e);
                        } finally {
                            cnfb.desconecta();
                        }
                    }

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }

                break;

            case DADOS_CREDITO:

                if (jTable2.getValueAt(linha, 3).toString() != null) {
                    consultaClassContas(jTable1, jTable2.getValueAt(linha, 3).toString());
                }

                break;

            case DADOS_DEBITO:
                if (jTable2.getValueAt(linha, 2).toString() != null) {
                    consultaClassContas(jTable3, jTable2.getValueAt(linha, 2).toString());
                }
                break;

        }

    }

    private String consultaContas(String codigo) {

        String resultado = "";

        sql = "SELECT nome, classificacao FROM cad_planocontas WHERE empresa = 1 "
                + "AND codigo = '" + codigo + "';";

        if (cnfb.conecta()) {
            try {
                cnfb.executeConsulta(sql);
                while (cnfb.rs.next()) {
                    resultado = cnfb.rs.getString("classificacao") + " - " + cnfb.rs.getString("nome");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            } finally {
                cnfb.desconecta();
            }
        }

        return resultado;
    }

    private void consultaClassContas(JTable x, String conta) {

        DefaultTableModel lista = (DefaultTableModel) x.getModel();

        lista.setRowCount(0);
        lista.setColumnCount(0);

        lista.addColumn("tipo");
        lista.addColumn("");

        x.getColumnModel().getColumn(0).setMaxWidth(0);
        x.getColumnModel().getColumn(0).setMinWidth(0);
        x.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
        x.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);

        x.setDefaultRenderer(Object.class, new ColorRender());

        sql = "select b.nome, b.classificacao, b.tipo "
                + "from cad_planocontas a "
                + "left join cad_planocontas b on (b.empresa = a.empresa and a.classificacao starting with (b.classificacao)) "
                + "where a.empresa = 1 and a.codigo = '" + conta + "' "
                + "ORDER BY b.classificacao, a.nome; ";

        if (cnfb.conecta()) {
            try {
                cnfb.executeConsulta(sql);
                while (cnfb.rs.next()) {
                    lista.addRow(new String[]{
                        cnfb.rs.getString("tipo"),
                        cnfb.rs.getString("classificacao") + " - " + cnfb.rs.getString("nome")
                    });
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            } finally {
                cnfb.desconecta();
            }
        }

    }

    private void gravaDados(String numero, String debito, String credito, String historico) throws Exception {
        boolean virgula = false;

        sql = "UPDATE ctb_lancamentos SET ";

        if (!"".equals(debito)) {

            sql += "debito = '" + debito + "' ";

            virgula = true;
        }

        if (!"".equals(credito)) {

            if (virgula) {
                sql += ", ";
            }

            sql += "credito = '" + credito + "' ";

            virgula = true;
        }

        if (!"".equals(historico)) {
            if (virgula) {
                sql += ", ";
            }

            sql += "historico = '" + historico + "' ";
        }

        sql += "WHERE empresa = 1 and numero = '" + numero + "';";

        cnfb.executeAtualizacao(sql);

    }

