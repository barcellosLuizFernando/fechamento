    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (jTable1.getRowCount() < 1) {
                throw new UnsupportedOperationException("Não há lançamentos a serem alterados.");
            }
            if (jTxtCodigoAtual.getText().equals(jTxtCodAnterior.getText())) {
                throw new UnsupportedOperationException("Município de origem é igual ao município de destino.");
            }
            if (jCbDestino.isSelected() == false && jCbOrigem.isSelected() == false) {
                throw new UnsupportedOperationException("Informe quais lançamentos serão alterados: ORIGEM ou DESTINO.");
            }

            try {
                ArrayList query = new ArrayList();
                cnfb.conecta();
                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    sql = "UPDATE com_entradas ";
                    if (jTable1.getValueAt(i, 2).toString().equals(jTxtCodAnterior.getText())) {
                        sql += "SET cod_mun_orig = '" + jTxtCodigoAtual.getText() + "' ";
                    } else {
                        sql += "SET cod_mun_dest = '" + jTxtCodigoAtual.getText() + "' ";

                    }

                    sql += "WHERE empresa = 1 and codigo = '" + jTable1.getValueAt(i, 0) + "';";

                    if ((jTable1.getValueAt(i, 2).toString().equals(jTxtCodAnterior.getText())) && jCbOrigem.isSelected()) {
                        //System.out.println(sql);
                        if ("S".equals(jTable1.getValueAt(i, 8))) {
                            query.add("UPDATE com_entradas SET liberado = 'N' WHERE empresa = 1 and codigo = '" + jTable1.getValueAt(i, 0) + "';");
                        }
                        query.add(sql);
                        if ("S".equals(jTable1.getValueAt(i, 8))) {
                            query.add("UPDATE com_entradas SET liberado = 'S' WHERE empresa = 1 and codigo = '" + jTable1.getValueAt(i, 0) + "';");
                        }
                    } else if (jCbDestino.isSelected()) {
                        if ("S".equals(jTable1.getValueAt(i, 8))) {
                            query.add("UPDATE com_entradas SET liberado = 'N' WHERE empresa = 1 and codigo = '" + jTable1.getValueAt(i, 0) + "';");
                        }
                        query.add(sql);
                        if ("S".equals(jTable1.getValueAt(i, 8))) {
                            query.add("UPDATE com_entradas SET liberado = 'S' WHERE empresa = 1 and codigo = '" + jTable1.getValueAt(i, 0) + "';");
                        }
                        //System.out.println(sql);
                    } else {
                        System.out.println("Não gerou nada! Destino: " + jCbDestino.isSelected());
                        System.out.println("Não gerou nada! Origem: " + (jTable1.getValueAt(i, 2).toString().equals(jTxtCodAnterior.getText())) + jCbOrigem.isSelected());
                    }

                    //   cnfb.executeAtualizacao(sql);
                }
                for (int j = 0; j < query.size(); j++) {
                    //System.out.println(query.get(j).toString());
                    cnfb.executeAtualizacao(query.get(j).toString());

                }
                montaTabela();
                JOptionPane.showMessageDialog(this, "Dados gravados com sucesso.", "Alteração de Municípios", JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            } finally {
                cnfb.desconecta();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Gravando dados", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed


