    private void montaLista() {
        int row_or = 0;
        int row_de = 0;
        try {

            DefaultTableModel lista = montaTabela();

            /**
             * SE NÃO TIVER TODOS OS DADOS INFORMADOS, NÃO PESQUISA.
             */
            if ("".equals(jTxtNomeAtual.getText())
                    | "".equals(jTxtNomeAnterior.getText())
                    | "  /  /    ".equals(jTxtDti.getText())
                    | "  /  /    ".equals(jTxtDti.getText())) {
                throw new UnsupportedOperationException("");
            }

            sql = "select com.CODIGO, com.DT_ENTRADA, com.COD_MUN_ORIG, "
                    + "m.NOME||' - '||m.UF as mun_orig, com.COD_MUN_DEST, "
                    + "m2.NOME||' - '||m2.UF as mun_dest, "
                    + "u.CODIGO||' - '||u.NOME as usuinc, "
                    + "u2.CODIGO||' - '||u2.NOME as usulib, com.LIBERADO "
                    + "from COM_ENTRADAS com "
                    + "left join CAD_MUNICIPIOS m on m.CODIGO = com.COD_MUN_ORIG "
                    + "left join CAD_MUNICIPIOS m2 on m2.CODIGO = com.COD_MUN_DEST "
                    + "left join CAD_USUARIOS u on u.CODIGO = com.USU_INC "
                    + "left join CAD_USUARIOS u2 on u2.CODIGO = com.USU_LIBERACAO "
                    + "where com.EMPRESA = 1 and "
                    + "com.DT_ENTRADA between '" + jTxtDti.getText().replace("/", ".") + "' and '" + jTxtDtf.getText().replace("/", ".") + "' and "
                    + "(com.COD_MUN_ORIG = '" + jTxtCodAnterior.getText() + "' or com.COD_MUN_DEST = '" + jTxtCodAnterior.getText() + "');";

            cnfb.conecta();
            cnfb.executeConsulta(sql);
            try {
                while (cnfb.rs.next()) {
                    lista.addRow(new String[]{
                        cnfb.rs.getString("codigo"),
                        tools.FormatNumbers.dateBrDefault(cnfb.rs.getDate("dt_entrada")),
                        cnfb.rs.getString("cod_mun_orig"),
                        cnfb.rs.getString("mun_orig"),
                        cnfb.rs.getString("cod_mun_dest"),
                        cnfb.rs.getString("mun_dest"),
                        cnfb.rs.getString("usuinc"),
                        cnfb.rs.getString("usulib"),
                        cnfb.rs.getString("liberado")
                    });
                    if (cnfb.rs.getString("cod_mun_orig").equals(jTxtCodAnterior.getText())) {
                        row_or++;
                    } else {
                        row_de++;
                    }
                }

            } catch (Exception e) {
            } finally {
                cnfb.desconecta();
            }

        } catch (Exception e) {
        }
        jTextField5.setText((row_de + row_or) + "");
        jTextField6.setText((row_or) + "");
        jTextField7.setText((row_de) + "");
    }

    private void incluiPesquisa() {

        try {

            cnfb.conecta();

            switch (var_pesquisa) {
                case 1:
                    if ("".equals(jTxtCodAnterior.getText())) {
                        throw new UnsupportedOperationException();
                    }
                    sql = "SELECT * FROM cad_municipios WHERE codigo = '" + jTxtCodAnterior.getText() + "';";
                    cnfb.executeConsulta(sql);
                    while (cnfb.rs.next()) {
                        jTxtNomeAnterior.setText(cnfb.rs.getString("nome") + " - " + cnfb.rs.getString("uf"));
                    }
                    break;

                case 2:
                    if ("".equals(jTxtCodigoAtual.getText())) {
                        throw new UnsupportedOperationException();
                    }
                    sql = "SELECT * FROM cad_municipios WHERE codigo = '" + jTxtCodigoAtual.getText() + "';";
                    cnfb.executeConsulta(sql);
                    while (cnfb.rs.next()) {
                        jTxtNomeAtual.setText(cnfb.rs.getString("nome") + " - " + cnfb.rs.getString("uf"));
                    }
                    break;
            }
        } catch (UnsupportedOperationException e) {
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Pesquisa de Município", JOptionPane.ERROR_MESSAGE);
        } finally {
            cnfb.desconecta();
        }
    }

