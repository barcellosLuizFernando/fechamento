    private void incluiPesquisa() {

        jTable1.setDefaultRenderer(Object.class, new ColorRender());
        jTable1.setDefaultRenderer(Double.class, new ColorRender());
        jTable1.setDefaultRenderer(String.class, new ColorRender());
        jTable1.setDefaultRenderer(Integer.class, new ColorRender());

        String pessoa = "";
        ArrayList lancamentos = new ArrayList();
        lancamentos.clear();

        DefaultTableModel lista = limpaTabela();
        cnfb = new ConexaoFB();

        if (jRadioButton1.isSelected()) {
            cnfb.arquivo = "fb.properties";
            cnfb.userlog = "167";
            empresa = 1;
        } else {
            cnfb.arquivo = "fb_tran.properties";
            cnfb.userlog = "131";
            empresa = 2;
        }

        try {

            if (jCheckBox2.isSelected()) {
                cn.iniciarTransacao();
                sql = "SELECT * FROM fechamento.fin_demissoes "
                        + "WHERE demissao between '" + tools.ManipulaData.dateUSstr(tools.ManipulaData.parseBrToDate("01/" + jTxtCompetence.getText())) + "' and "
                        + "'" + tools.ManipulaData.dateUSstr(tools.ManipulaData.lastDay(jTxtCompetence.getText())) + "' and "
                        + "empresa = " + empresa + ";";
                cn.executeConsulta(sql);
                while (cn.rs.next()) {
                    lista.addRow(new Object[]{
                        "T",
                        cn.rs.getInt("numero"),
                        cn.rs.getInt("pessoa"),
                        cn.rs.getInt("matricula"),
                        tools.ManipulaData.dateBRstr(cn.rs.getDate("demissao")),
                        cn.rs.getDouble("valor"),
                        cn.rs.getInt("conta")
                    });

                    lancamentos.add(cn.rs.getString("pessoa") + cn.rs.getString("matricula"));

                }
                cn.finalizarTransacao();
                sql = "select * from (select t.CODIGO, t.NOME, tc.ID_COOPERADO, tc.CONTA_COTA_CAPITAL, "
                        + "tch.\"DATA\", "
                        + "(select coalesce(sum(case when c.DEBITO = tc.CONTA_COTA_CAPITAL then c.valor*-1 else c.VALOR end),0) "
                        + "from CTB_LANCAMENTOS c  "
                        + "where c.EMPRESA = tc.EMPRESA and  "
                        //+ "c.\"DATA\" <= '" + tools.ManipulaData.lastDayToBR(jTxtCompetence.getText()).replace("/", ".") + "' and  "
                        + "(c.DEBITO = tc.CONTA_COTA_CAPITAL or c.CREDITO = tc.CONTA_COTA_CAPITAL)) as valor "
                        + "from CAD_TERCEIROS_COOPERADOS tc "
                        + "inner join CAD_TERCEIROS_COOPERADOS_HIST tch on tch.EMPRESA = tc.EMPRESA and  "
                        + "    tch.CODIGO = tc.CODIGO and tch.TIPO = 'D' and tch.\"DATA\" between '01." + jTxtCompetence.getText().replace("/", ".") + "' and '" + tools.ManipulaData.lastDayToBR(jTxtCompetence.getText()).replace("/", ".") + "' "
                        + "left join cad_terceiros t on t.EMPRESA = tc.EMPRESA and tc.CODIGO = t.CODIGO "
                        + "order by t.NOME) as demi "
                        + "where demi.valor > 0.00;";
            } else {
                sql = "select * from ( "
                        + "    select t.CODIGO, t.NOME, tc.ID_COOPERADO, tc.CONTA_COTA_CAPITAL,"
                        + "     current_date as data, "
                        + "    (select coalesce(sum(case when c.DEBITO = tc.CONTA_COTA_CAPITAL then c.valor*-1 else c.VALOR end),0)"
                        + "        from CTB_LANCAMENTOS c"
                        + "        where c.EMPRESA = tc.EMPRESA and"
                        + "        (c.DEBITO = tc.CONTA_COTA_CAPITAL or c.CREDITO = tc.CONTA_COTA_CAPITAL)) as valor"
                        + "    from CAD_TERCEIROS_COOPERADOS tc"
                        + "    inner join cad_terceiros t on t.EMPRESA = tc.EMPRESA and tc.CODIGO = t.CODIGO and DateDiff(Year, t.dt_nascimento, Current_Date) >= 65 and t.pessoa_fj = 'F'"
                        + "    where tc.empresa = 1 and (select cooperado from ctb_get_config_contabil_terc(t.empresa, t.codigo, current_date)) = 'S'"
                        + "    order by t.NOME ) as nono "
                        + "where nono.valor > '" + jTextField1.getText().replace(".", "").replace(",", ".") + "';";
            }

            cnfb.conecta();
            cnfb.executeConsulta(sql);
            while (cnfb.rs.next()) {
                if (!lancamentos.contains(cnfb.rs.getString("codigo") + cnfb.rs.getInt("id_cooperado"))) {
                    lista.addRow(new Object[]{
                        "A",
                        "0",
                        cnfb.rs.getInt("codigo"),
                        cnfb.rs.getInt("id_cooperado"),
                        tools.ManipulaData.dateBRstr(cnfb.rs.getDate("data")),
                        cnfb.rs.getDouble("valor"),
                        cnfb.rs.getInt("conta_cota_capital"),
                        cnfb.rs.getString("nome")
                    });
                    pessoa = cnfb.rs.getString("codigo") + " - " + cnfb.rs.getString("nome");
                }
            }
            cnfb.rs.close();

        } catch (Exception e) {
            tools.DefaultMsg.errorMsg(e.getMessage() + ".\n Cadastro: " + pessoa);
        } finally {
            cnfb.desconecta();
        }

    }

