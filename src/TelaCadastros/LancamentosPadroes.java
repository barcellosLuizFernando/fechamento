    private void MontaLista() {

        String sql;

        DefaultTableModel lista = (DefaultTableModel) jTblConsulta_Multi.getModel();

        lista.setColumnCount(0);
        lista.setRowCount(0);

        switch (var_consulta) {
            case 1:

                lista.addColumn("Código");
                lista.addColumn("Descrição");

                jTblConsulta_Multi.getColumnModel().getColumn(0).setMaxWidth(60);
                jTblConsulta_Multi.getColumnModel().getColumn(0).setMinWidth(60);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(60);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(0).setMinWidth(60);

                if ("".equals(jTxtPesquisa_Multi2.getText())) {
                    sql = "select * from lanc_padrao ORDER BY descricao;";
                } else {
                    sql = "select * from lanc_padrao "
                            + "where UPPER(descricao) "
                            + "LIKE '%" + jTxtPesquisa_Multi2.getText().toUpperCase() + "%' order by descricao;";
                }

                try {
                cn.iniciarTransacao();
                cn.executeConsulta(sql);

                    while (cn.rs.next()) {
                        lista.addRow(new String[]{
                            cn.rs.getString("id"),
                            cn.rs.getString("descricao")});

                    }
                } catch (SQLException ex) {
                    Logger.getLogger(QuebraTecnica.class
                            .getName()).log(Level.SEVERE, null, ex);
                }

                cn.finalizarTransacao();

                break;

            case 2:
                PesquisaContas();
                break;

            case 3:
                PesquisaContas();
                break;
            case 4:
                PesquisaContas();
                break;

            case 5:

                lista.addColumn("Código");
                lista.addColumn("Descrição");

                jTblConsulta_Multi.getColumnModel().getColumn(0).setMaxWidth(60);
                jTblConsulta_Multi.getColumnModel().getColumn(0).setMinWidth(60);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(60);
                jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(0).setMinWidth(60);

                if (!"".equals(jTxtPesquisa_Multi2.getText())) {
                    sql = "select codigo, nome from cad_hist_contabil where empresa = 1 and UPPER(nome) "
                            + "LIKE '%" + jTxtPesquisa_Multi2.getText().toUpperCase() + "%' order by nome;";
                } else {
                    sql = "select codigo, nome from cad_hist_contabil order by nome;";
                }

                cnfb.conecta();

                try {
                    cnfb.executeConsulta(sql);
                    while (cnfb.rs.next()) {
                        lista.addRow(new String[]{
                            cnfb.rs.getString("codigo"),
                            cnfb.rs.getString("nome")});

                    }
                } catch (SQLException ex) {
                    Logger.getLogger(QuebraTecnica.class
                            .getName()).log(Level.SEVERE, null, ex);
                }
                cnfb.desconecta();

                break;

            default:
                JOptionPane.showMessageDialog(this, "Consulta não implementada!");
                break;

        }

    }

    private void PesquisaContas() {

        String likepc;
        if (!"".equals(jTxtPesquisa_Multi2.getText())) {
            likepc = "where UPPER(nome) "
                    + "LIKE '%" + jTxtPesquisa_Multi2.getText().toUpperCase() + "%'";
        } else {
            likepc = "";
        }

        DefaultTableModel lista = (DefaultTableModel) jTblConsulta_Multi.getModel();

        lista.addColumn("Código");
        lista.addColumn("Tipo");
        lista.addColumn("Clasificação");
        lista.addColumn("Nome");

        jTblConsulta_Multi.getColumnModel().getColumn(0).setMaxWidth(60);
        jTblConsulta_Multi.getColumnModel().getColumn(0).setMinWidth(60);
        jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(60);
        jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(0).setMinWidth(60);
        jTblConsulta_Multi.getColumnModel().getColumn(1).setMaxWidth(40);
        jTblConsulta_Multi.getColumnModel().getColumn(1).setMinWidth(40);
        jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(1).setMaxWidth(40);
        jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(1).setMinWidth(40);
        jTblConsulta_Multi.getColumnModel().getColumn(2).setMaxWidth(180);
        jTblConsulta_Multi.getColumnModel().getColumn(2).setMinWidth(180);
        jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(2).setMaxWidth(180);
        jTblConsulta_Multi.getTableHeader().getColumnModel().getColumn(2).setMinWidth(180);

        String sql2 = "with planocontas as (select a.codigo, a.tipo, "
                + "a.classificacao, a.nome from cad_planocontas A "
                + "where a.ativo != 'N' and a.tipo = 'T' "
                + "union all select a.codigo, a.tipo, a.classificacao, "
                + "a.nome from cad_planocontas a "
                + "where a.ativo != 'N' and a.tipo = 'A' "
                + ") "
                + "select * from planocontas "
                + likepc
                + "order by classificacao;";

        cnfb.conecta();

        try {
            cnfb.executeConsulta(sql2);
            while (cnfb.rs.next()) {
                lista.addRow(new String[]{
                    cnfb.rs.getString("codigo"),
                    cnfb.rs.getString("tipo"),
                    cnfb.rs.getString("classificacao"),
                    cnfb.rs.getString("nome")});

            }
        } catch (SQLException ex) {
            Logger.getLogger(QuebraTecnica.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
        cnfb.desconecta();
    }

