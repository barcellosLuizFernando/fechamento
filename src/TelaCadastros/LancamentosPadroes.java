    private void jBtnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGravarActionPerformed
        String id = jTxtId.getText();
        String descricao = jTxtDescricao.getText();
        String debito = jTxtDebP.getText();
        String debitoprod = jTxtDebProd.getText();
        String credito = jTxtCred.getText();
        String historico = jTxtHistorico.getText();
        String complemento = jTxtComplemento.getText();

        String sql;
        try {
            cn.iniciarTransacao();
            if ("Cancelar".equals(jBtnIncluir.getText())) {
                sql = "INSERT INTO lanc_padrao (descricao,cdebpad,cdebprod,ccred,"
                        + "historico,complemento) VALUES ('" + descricao + "',"
                        + "'" + debito + "','" + debitoprod + "','" + credito + "',"
                        + "'" + historico + "','" + complemento + "');";
            } else {
                sql = "UPDATE lanc_padrao set descricao = '" + descricao + "', "
                        + "cdebpad = '" + debito + "', cdebprod = '" + debitoprod + "',"
                        + "ccred = '" + credito + "', historico = '" + historico + "',"
                        + "complemento = '" + complemento + "'"
                        + "where id = '" + id + "';";
            }
            cn.executeAtualizacao(sql);
            DesabilitarEdicao();
        } catch (Exception e) {
            tools.DefaultMsg.errorMsg(e.getMessage());
        } finally {
            cn.finalizarTransacao();
        }

        String sql9 = "select max(id) from lanc_padrao;";
        if ("NOVO".equals(id)) {
            try {
                cn.iniciarTransacao();
                cn.executeConsulta(sql9);
                while (cn.rs.next()) {
                    jTxtId.setText(cn.rs.getString(1));
                }
            } catch (SQLException ex) {
                Logger.getLogger(LancamentosPadroes.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                cn.finalizarTransacao();
            }
        }


    }//GEN-LAST:event_jBtnGravarActionPerformed

