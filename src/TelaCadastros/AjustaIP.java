    private void montaTabela() {
        DefaultTableModel lista = (DefaultTableModel) jTable1.getModel();

        lista.setRowCount(0);
        lista.setColumnCount(0);

        lista.addColumn("Transação");
        lista.addColumn("Endereço");
        lista.addColumn("Login");

    }

    private void montaLista() {
        DefaultTableModel lista = (DefaultTableModel) jTable1.getModel();

        ConexaoFB_Log.arquivo = "fb_log.properties";

        cnfb.conecta();
        cnfb.executeConsulta("select distinct * from log_tables a where a.usuario in (167,131) and a.ip not in ('127.0.0.1','192.168.1.99');");
        try {
            while (cnfb.rs.next()) {
                lista.addRow(new String[]{
                    cnfb.rs.getString("codigo"),
                    cnfb.rs.getString("ip"),
                    cnfb.rs.getString("usuario")
                });

            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Não foi possível realizar a consulta.");
        }
        cnfb.desconecta();

        new TelaPrincipal().defineBD();
    }

