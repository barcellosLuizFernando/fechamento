    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        jTable1.selectAll();

        int linha = 0;
        
        int linhas = jTable1.getRowCount();
        cnfb.conecta();
        for (int x = linhas; x != 0; x--) {
            String id = jTable1.getValueAt(linha, 0).toString();

            cnfb.executeAtualizacao("UPDATE log_tables SET ip = '127.0.0.1' WHERE codigo = " + id + ";");
            
            linha++;
        }
        cnfb.desconecta();
        
        montaTabela();
        montaLista();
    }//GEN-LAST:event_jButton3ActionPerformed


