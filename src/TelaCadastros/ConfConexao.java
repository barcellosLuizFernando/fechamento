    private void jBtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBuscarActionPerformed
        arq_conexao = jCBConexao.getSelectedItem().toString();

        if (jCheckBox1.isSelected()) {
            arq_conexao += "2";
        }

        props = new Properties();

        file = null;

        try {
            if (null != arq_conexao) {
                switch (arq_conexao) {
                    case "FireBird":
                        arquivo = "./src/properties/fb.properties";
                        database = FIREBIRD;
                        break;
                    case "FireBird2":
                        arquivo = "./src/properties/fbcasa.properties";
                        database = FIREBIRD;
                        break;
                    case "MySQL":
                        arquivo = "./src/properties/mysql.properties";
                        database = MYSQL;
                        break;
                    case "MySQL2":
                        arquivo = "./src/properties/mysqlcasa.properties";
                        database = MYSQL;
                        break;
                    case "FB TRR":
                        arquivo = "./src/properties/fbtrr.properties";
                        database = FIREBIRD;
                        break;
                    case "FB TRR2":
                        arquivo = "./src/properties/fbtrrcasa.properties";
                        database = FIREBIRD;
                        break;
                    case "FB Transcooper":
                        arquivo = "./src/properties/fb_tran.properties";
                        database = FIREBIRD;
                        break;
                    case "FB Transcooper2":
                        arquivo = "./src/properties/fb_tran_casa.properties";
                        database = FIREBIRD;
                        break;
                    default:
                        JOptionPane.showMessageDialog(this, "Não foi selecionada uma conexão válida.");
                        break;
                }
            }

            file = new FileInputStream(arquivo);
            props.load(file);
            file.close();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Não foi possível abrir o arquivo de configurações.");
        }

        jTxtBD.setText(props.getProperty("url"));
        jTxtUser.setText(props.getProperty("user"));
        jTxtcharset.setText(props.getProperty("charset"));
        jPwd.setText(props.getProperty("password"));
        jTxtDriver.setText(props.getProperty("driver"));
        jTxtCtype.setText(props.getProperty("lc_ctype"));


    }//GEN-LAST:event_jBtnBuscarActionPerformed

