    private void jBtnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGravarActionPerformed
        props.setProperty("user", jTxtUser.getText());
        props.setProperty("url", jTxtBD.getText());
        props.setProperty("charset", jTxtcharset.getText());
        props.setProperty("password", jPwd.getText());
        props.setProperty("driver", jTxtDriver.getText());
        props.setProperty("lc_ctype", jTxtCtype.getText());

        try {
            FileOutputStream out = new FileOutputStream(arquivo);
            props.store(out, "");
            out.close();

            JOptionPane.showMessageDialog(this, "Dados gravados com sucesso.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Não foi possível gravar os dados corretamente.");
        }

    }//GEN-LAST:event_jBtnGravarActionPerformed

    private void jBtnTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnTestActionPerformed
        try {
            if (database == FIREBIRD) {

                conn.ConexaoFB cnfb = new ConexaoFB();
                cnfb.arquivo = this.arquivo.replace("./src/properties/", "");

                if (cnfb.conecta()) {
                    cnfb.desconecta();
                } else {
                    throw new UnsupportedOperationException();
                }

            } else if (database == MYSQL) {
                conn.ConexaoMySQL cn = new ConexaoMySQL();
                cn.arquivo = this.arquivo.replace("./src/properties/", "");
                cn.conecta();
                cn.desconecta();
            }

            JOptionPane.showMessageDialog(this, "Conexão validada com sucesso!", "Teste de Conexão", JOptionPane.INFORMATION_MESSAGE);

        } catch (UnsupportedOperationException e) {
        } catch (Exception e) {
            tools.DefaultMsg.errorMsg(e.getMessage());
        }
    }//GEN-LAST:event_jBtnTestActionPerformed


