    DefaultTableModel lista = new DefaultTableModel();
    
    public void MontaLista() {
        
        if (!"".equals(txtPesquisar.getText())) {
            lista.setColumnCount(0);  //limpa a tabela
            lista.setRowCount(0);     //limpa a tabela

            try {
                cn.iniciarTransacao();
                String sql = "SELECT * FROM cad_atividades "
                        + "WHERE UPPER(nome) "
                        + "LIKE '%" + txtPesquisar.getText().toUpperCase() + "%' OR descricao "
                        + "LIKE '%" + txtPesquisar.getText() + "%'"
                        + " ORDER BY nome";
                
                cn.executeConsulta(sql);
                
                lista.addColumn("Código");
                lista.addColumn("Nome");
                lista.addColumn("Descição");
                lista.addColumn("Tipo");
                lista.addColumn("Periodicidade");
                lista.addColumn("Dependencia");
                lista.addColumn("Classificação");
                
                while (cn.rs.next()) {
                    
                    lista.addRow(new String[]{
                        cn.rs.getString("codigo"),
                        cn.rs.getString("nome"),
                        cn.rs.getString("descricao"),
                        cn.rs.getString("tipo"),
                        cn.rs.getString("periodicidade"),
                        cn.rs.getString("dependencia"),
                        cn.rs.getString("classificacao")});
                }
            } catch (SQLException ex) {
                
                JOptionPane.showMessageDialog(null, "Erro ao consultar o banco de dados: " + ex);
            } finally {
                cn.finalizarTransacao();
            }
            
            tabCadAtividades.setModel(lista);
            /* Abaixo, configuração para ocultar colunas na jTable
             * Porém, quando clica, aparecem os dados para edição e consulta
             */
            tabCadAtividades.getColumnModel().getColumn(2).setMaxWidth(0);
            tabCadAtividades.getColumnModel().getColumn(2).setMinWidth(0);
            tabCadAtividades.getTableHeader().getColumnModel().getColumn(2).setMaxWidth(0);
            tabCadAtividades.getTableHeader().getColumnModel().getColumn(2).setMinWidth(0);
            tabCadAtividades.getColumnModel().getColumn(0).setMaxWidth(0);
            tabCadAtividades.getColumnModel().getColumn(0).setMinWidth(0);
            tabCadAtividades.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
            tabCadAtividades.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);
            
        } else { //Configuração para quando não há texto no campo pesquisa
            lista.setColumnCount(0);  //limpa a tabela
            lista.setRowCount(0);     //limpa a tabela
            cn.iniciarTransacao();
            String sql = "SELECT * FROM cad_atividades "
                    + " ORDER BY classificacao";
            
            try {
                cn.executeConsulta(sql);
                
                lista.addColumn("Código");
                lista.addColumn("Nome");
                lista.addColumn("Descrição");
                lista.addColumn("Tipo");
                lista.addColumn("Periodicidade");
                lista.addColumn("Dependencia");
                lista.addColumn("Classificação");
                
                while (cn.rs.next()) {
                    
                    lista.addRow(new String[]{
                        cn.rs.getString("codigo"),
                        cn.rs.getString("nome"),
                        cn.rs.getString("descricao"),
                        cn.rs.getString("tipo"),
                        cn.rs.getString("periodicidade"),
                        cn.rs.getString("dependencia"),
                        cn.rs.getString("classificacao")});
                    
                }
            } catch (SQLException ex) {
                
                JOptionPane.showMessageDialog(null, "Erro ao consultar o banco de dados: " + ex);
            } finally {
                cn.finalizarTransacao();
                
            }
            tabCadAtividades.setModel(lista);

            /* Abaixo, configuração para ocultar colunas na jTable
             * Porém, quando clica, aparecem os dados para edição e consulta
             */
            tabCadAtividades.getColumnModel().getColumn(2).setMaxWidth(0);
            tabCadAtividades.getColumnModel().getColumn(2).setMinWidth(0);
            tabCadAtividades.getTableHeader().getColumnModel().getColumn(2).setMaxWidth(0);
            tabCadAtividades.getTableHeader().getColumnModel().getColumn(2).setMinWidth(0);
            tabCadAtividades.getColumnModel().getColumn(0).setMaxWidth(0);
            tabCadAtividades.getColumnModel().getColumn(0).setMinWidth(0);
            tabCadAtividades.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
            tabCadAtividades.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);

            //Configuração dos botões, quando não há pesquisa
            btnEditarAtv.setEnabled(false);
            btnCadastrarAtv.setEnabled(true);
        }
    }
    
