    private void btnGravarAtvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarAtvActionPerformed
        // TODO add your handling code here:
        if (!"".equals(txtClassificacaoAtv.getText())
                && !"".equals(txtNomeAtv.getText())
                && !"".equals(txtDescricaoAtv.getText()) /*&& !"".equals(txtIniAtv.getText())*/) {
            
            String classAtv = txtClassificacaoAtv.getText();
            String dependAtv = txtDependenciaAtv.getSelectedItem().toString();
            String descrAtv = txtDescricaoAtv.getText();
            String nomeAtv = txtNomeAtv.getText();
            String tipoAtv = txtTipoAtv.getSelectedItem().toString();
            String dtIncAtv = txtDtInclAtv.getText();
            String dtFimAtv = txtFimAtv.getText();
            String perAtv = txtPeriodoAtv.getSelectedItem().toString();
            String dtCad = txtDtInclAtv.getText();
            //String statusAtv = txtStatusAtv.getText();

            try {
                cn.iniciarTransacao();
                if (txtStatusAtv.getText() == "CADASTRANDO") {
                    sql = "INSERT INTO CAD_ATIVIDADES (NOME, CLASSIFICACAO, TIPO, DESCRICAO, PERIODICIDADE, DEPENDENCIA, DATA_CADASTRO, DATA_INI_VIGENCIA) VALUES ('" + nomeAtv + "','" + classAtv + "','" + tipoAtv + "','" + descrAtv + "','" + perAtv + "','" + dependAtv + "','" + dtCad + "','" + dtIncAtv + "')";
                } else {
                    sql = "UPDATE CAD_ATIVIDADES SET NOME = '" + nomeAtv + "', CLASSIFICACAO = '" + classAtv + "', TIPO = '" + tipoAtv + "', DESCRICAO = '" + descrAtv + "', PERIODICIDADE = '" + perAtv + "', DEPENDENCIA = '" + dependAtv + "' WHERE codigo = " + Integer.parseInt(txtCodAtv.getText());
                    //        + "(NOME, DEPARTAMENTO, FUNCAO, ATIVO) VALUES ('" + nomeUser
                }
                    cn.executeAtualizacao(sql);
            } catch (SQLException e) {
                tools.DefaultMsg.errorMsg(e.getMessage());
            } finally {
                cn.finalizarTransacao();
            }
            
            txtClassificacaoAtv.setEnabled(false);
            txtCodAtv.setText(" ");
            txtStatusAtv.setText(" ");
            txtDependenciaAtv.setEnabled(false);
            txtDescricaoAtv.setEnabled(false);
            txtIniAtv.setEnabled(false);
            txtNomeAtv.setEnabled(false);
            txtPeriodoAtv.setEnabled(false);
            txtPesquisar.setEnabled(true);
            txtTipoAtv.setEnabled(false);
            btnCancelarAtv.setEnabled(false);
            btnGravarAtv.setEnabled(false);
            btnCadastrarAtv.setEnabled(true);
            MontaLista();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Algum campo está em Branco.");
        }
    }//GEN-LAST:event_btnGravarAtvActionPerformed

