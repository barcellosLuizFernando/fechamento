    private int defineEmpresa() {

        if (cn.iniciarTransacao()) {
            try {
                cn.executeConsulta("SELECT iD FROM empresa WHERE nome = '" + jCbEmpresa.getSelectedItem() + "';");
                while (cn.rs.next()) {
                    empresa = cn.rs.getInt("id");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            } finally {
                cn.finalizarTransacao();
            }
        }

        //DEFINE CAMINNHO DO BANCO DE DADOS QUE SERÁ CONECTADO
        if (cn.iniciarTransacao()) {
            try {
                cn.executeConsulta("SELECT local FROM empresa WHERE id = '" + empresa + "';");
                while (cn.rs.next()) {
                    caminho = cn.rs.getString("local");
                }
            } catch (Exception e) {
                importar = 1;
            } finally {
                cn.finalizarTransacao();
            }
        }

        return empresa;
    }

