    public void MontaLista() {
        
        txtDia.setValue(0);
        
        DefaultTableModel lista = (DefaultTableModel) tabCadPrazo.getModel();
        
        lista.setColumnCount(0);  //limpa a tabela
        lista.setRowCount(0);     //limpa a tabela
        lista.addColumn("Cod");
        lista.addColumn("Class");
        lista.addColumn("Atividade");
        lista.addColumn("Dia");
        lista.addColumn("CodPrazo");
        lista.addColumn("dt");
        
        tabCadPrazo.getColumnModel().getColumn(0).setMaxWidth(40);
        tabCadPrazo.getColumnModel().getColumn(0).setMinWidth(40);
        tabCadPrazo.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(40);
        tabCadPrazo.getTableHeader().getColumnModel().getColumn(0).setMinWidth(40);
        
        tabCadPrazo.getColumnModel().getColumn(1).setMaxWidth(60);
        tabCadPrazo.getColumnModel().getColumn(1).setMinWidth(60);
        tabCadPrazo.getTableHeader().getColumnModel().getColumn(1).setMaxWidth(60);
        tabCadPrazo.getTableHeader().getColumnModel().getColumn(1).setMinWidth(60);
        
        tabCadPrazo.getColumnModel().getColumn(4).setMaxWidth(0);
        tabCadPrazo.getColumnModel().getColumn(4).setMinWidth(0);
        tabCadPrazo.getTableHeader().getColumnModel().getColumn(4).setMaxWidth(0);
        tabCadPrazo.getTableHeader().getColumnModel().getColumn(4).setMinWidth(0);
        
        tabCadPrazo.getColumnModel().getColumn(3).setMaxWidth(40);
        tabCadPrazo.getColumnModel().getColumn(3).setMinWidth(40);
        tabCadPrazo.getTableHeader().getColumnModel().getColumn(3).setMaxWidth(40);
        tabCadPrazo.getTableHeader().getColumnModel().getColumn(3).setMinWidth(40);
        
        tabCadPrazo.getColumnModel().getColumn(5).setMaxWidth(0);
        tabCadPrazo.getColumnModel().getColumn(5).setMinWidth(0);
        tabCadPrazo.getTableHeader().getColumnModel().getColumn(5).setMaxWidth(0);
        tabCadPrazo.getTableHeader().getColumnModel().getColumn(5).setMinWidth(0);
        
        cn.iniciarTransacao();
        
        if (!"".equals(txtPesquisar.getText())) {
            String sql = "SELECT a.codigo, a.nome, a.classificacao, p.codigo as codprazo, p.dia, p.dt_cadastro, p.dt_ini_vigencia"
                    + " FROM cad_atividades a"
                    + " left join cad_prazo p on (p.atividade = a.codigo)"
                    + "WHERE UPPER(nome) "
                    + "LIKE '%" + txtPesquisar.getText().toUpperCase() + "%'"
                    + " ORDER BY classificacao";
            
            try {
                cn.executeConsulta(sql);
                
                while (cn.rs.next()) {
                    
                    lista.addRow(new String[]{
                        cn.rs.getString("codigo"),
                        cn.rs.getString("classificacao"),
                        cn.rs.getString("nome"),
                        cn.rs.getString("dia"),
                        cn.rs.getString("codprazo"),
                        cn.rs.getString("dt_ini_vigencia")});
                }
            } catch (SQLException ex) {
                
                JOptionPane.showMessageDialog(null, "Erro ao consultar o banco de dados: " + ex);
            }
            tabCadPrazo.setModel(lista);
            btnEditarPrazo.setEnabled(false);
            btnCadastrarPrazo.setEnabled(false);
            
        } else { //Configuração para quando não há texto no campo pesquisa

            txtStatus.setText("");
            btnCadastrarPrazo.setEnabled(false);
            btnEditarPrazo.setEnabled(false);
            
            String sql = "SELECT a.codigo, a.nome, a.classificacao, p.codigo as codprazo, p.dia, p.dt_cadastro, p.dt_ini_vigencia "
                    + "FROM cad_atividades a"
                    + " left join cad_prazo p on (p.atividade = a.codigo)"
                    + " ORDER BY a.classificacao";
            
            try {
                cn.executeConsulta(sql);
                
                while (cn.rs.next()) {
                    
                    lista.addRow(new String[]{
                        cn.rs.getString("codigo"),
                        cn.rs.getString("classificacao"),
                        cn.rs.getString("nome"),
                        cn.rs.getString("dia"),
                        cn.rs.getString("codprazo"),
                        cn.rs.getString("dt_ini_vigencia")});
                }
            } catch (SQLException ex) {
                
                JOptionPane.showMessageDialog(null, "Erro ao consultar o banco de dados: " + ex);
            }
        }
        cn.finalizarTransacao();
    }
    
    public void Selecao() {
        
        int linha = tabCadPrazo.getSelectedRow();
        
        Object dia = tabCadPrazo.getValueAt(linha, 3);
        txtCodAtv.setText(tabCadPrazo.getValueAt(linha, 0).toString());
        txtNomeAtv.setText(tabCadPrazo.getValueAt(linha, 2).toString());
        
        txtStatus.setText("" + dia);
        
        if (dia != null) {
            
            Date dt_vig = null;            
            
            try {
                dt_vig = dateOut.parse(tabCadPrazo.getValueAt(linha, 5).toString().replace("-", "/"));
            } catch (ParseException ex) {
                Logger.getLogger(CadPrazos.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            String vigencia = dateIn.format(dt_vig);
            
            txtCodPrazo.setText(tabCadPrazo.getValueAt(linha, 4).toString());
            txtDia.setValue(Integer.parseInt(dia + ""));
            try {
                txtDtVigencia.setValue(dateIn.parse(vigencia));
            } catch (ParseException ex) {
                Logger.getLogger(CadPrazos.class.getName()).log(Level.SEVERE, null, ex);
            }
            btnEditarPrazo.setEnabled(true);
            btnCadastrarPrazo.setEnabled(false);
            txtStatus.setText("CADASTRADO");
        } else {
            btnCadastrarPrazo.setEnabled(true);
            btnEditarPrazo.setEnabled(false);
            txtStatus.setText("SEM PRAZO");
            txtCodPrazo.setText("");
            txtDia.setValue(0);
            //JOptionPane.showMessageDialog(null, "Prazo não cadastrado: " + e);
        }
    }

    private void btnGravarPrazoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarPrazoActionPerformed
        String dia = txtDia.getValue().toString();
        
        if (!"0".equals(dia)) {
            
            String codAtv = txtCodAtv.getText();
            String dtIniVigencia = dateOut.format(txtDtVigencia.getValue());
            
            String sql;
            
            if ("CADASTRANDO".equals(txtStatus.getText())) {
                sql = "INSERT INTO CAD_PRAZO (ATIVIDADE, DIA, DT_CADASTRO, DT_INI_VIGENCIA) VALUES ('" + codAtv + "','" + dia + "',current_date(),'" + dtIniVigencia + "')";
            } else {
                sql = "UPDATE CAD_PRAZO SET DIA = '" + dia + "', DT_INI_VIGENCIA = '" + dtIniVigencia + "' WHERE codigo = " + Integer.parseInt(txtCodPrazo.getText());
                
            }
            
            try {
                cn.iniciarTransacao();
                cn.executeAtualizacao(sql);
            } catch (SQLException e) {
                tools.DefaultMsg.errorMsg(e.getMessage());
            } finally {
                cn.finalizarTransacao();
            }
            
        } else {
            JOptionPane.showMessageDialog(rootPane, "Dia não definido.");
        }
        
        int resultadoBD = ConexaoMySQL.resultadoUpd;
        
        if (resultadoBD == 0
                && !"0".equals(dia)) {
            DesabilitaEdicao();
            MontaLista();
        } else {
            JOptionPane.showMessageDialog(this, "Dados não gravados!");
        }
    }//GEN-LAST:event_btnGravarPrazoActionPerformed

