    private DefaultTableModel montaTabela() {
        DefaultTableModel lista = (DefaultTableModel) jTable1.getModel();

        lista.setColumnCount(0);
        lista.setRowCount(0);

        lista.addColumn("Encargo");
        lista.addColumn("Competência");
        lista.addColumn("Unidade");
        lista.addColumn("Código");
        lista.addColumn("Valor");
        lista.addColumn("Financeiro");

        jTable1.setDefaultRenderer(Object.class, new ColorRender());

        return lista;
    }

    private void incluiPesquisa(String encargo) {
        DefaultTableModel lista = montaTabela();

        sql = "SELECT a.encargo, a.competencia, a.nomfil, a.unidade, a.financeiro, sum(a.valeve) as valeve "
                + "FROM fechamento.folha_provisao a "
                + "WHERE a.competencia = '" + jTxtCompetencia.getText() + "' "
                + "AND a.encargo = '" + encargo + "' "
                + "GROUP BY a.nomfil;";

        if (cn.iniciarTransacao()) {
            try {
                cn.executeConsulta(sql);
                while (cn.rs.next()) {
                    lista.addRow(new String[]{
                        cn.rs.getString("encargo"),
                        cn.rs.getString("competencia"),
                        cn.rs.getString("nomfil"),
                        cn.rs.getString("unidade"),
                        df.format(cn.rs.getDouble("valeve")),
                        cn.rs.getString("financeiro")
                    });
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                cn.finalizarTransacao();
            }
        }
        somaDados();
    }

    private void somaDados() {
        int qtd = 0;
        Double total = 0.00;

        for (int i = 0; i < jTable1.getRowCount(); i++) {
            qtd++;
            total += Double.parseDouble(jTable1.getValueAt(i, 4).toString().replace(".", "").replace(",", "."));
        }

        jTextField1.setText(qtd + "");
        jTextField2.setText(df.format(total));

    }

    private void gravarDados(final boolean b) {

        final IntegraEncargos p = this;
        p.setVisible(false);

        new Thread() {
            public void run() {
                TelaDeAvisos tela = null;
                try {
                    tela = new TelaDeAvisos();

                    System.out.println("\nPreparando para gravar dados.");
                    String competencia = jTxtCompetencia.getText();
                    String unidade;
                    String valor;
                    String encargo = "";
                    String financeiro = "";

                    tela.setMsgStatus("Gravando financeiro...", 0, 0);
                    gravar:
                    for (int i = 0; i < jTable1.getRowCount(); i++) {
                        tela.setProgresso(i + 1, jTable1.getRowCount());

                        System.out.println("Carregando variáveis.");
                        encargo = jTable1.getValueAt(i, 0).toString();
                        competencia = jTable1.getValueAt(i, 1).toString();
                        unidade = jTable1.getValueAt(i, 3).toString();
                        valor = jTable1.getValueAt(i, 4).toString().replace(".", "").replace(",", ".");

                        try {
                            financeiro = jTable1.getValueAt(i, 5).toString();
                        } catch (Exception e) {
                            financeiro = "";
                        }

                        System.out.println("Varíáveis carregadas.");

                        if ((financeiro == null || financeiro == "") && b) {
                            System.out.println("Chamando classe de lançamentos.");
                            if (prov.lancaFinanceiro(unidade, valor, competencia, encargo) == false) {
                                break gravar;
                            }
                        } else if ((financeiro != null || financeiro != "") && b == false) {
                            if (prov.deletaFinanceiro(financeiro) == false) {
                                break gravar;
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "A tabela não contém dados financeiros válidos.");

                            break gravar;
                        }
                    }

                    incluiPesquisa(encargo);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                } finally {
                    tela.dispose();
                    p.setVisible(true);
                }
            }
        }.start();
    }
