    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String encargo;
        String competencia = jTxtCompetencia.getText();
        String contaContabil = "";

        if (jRbFGTS.isSelected()) {
            encargo = "FGTS";
            contaContabil = "1573";
        } else if (jRbINSS.isSelected()) {
            encargo = "INSS";
            contaContabil = "1579";
        } else if (jRbFUNRURAL.isSelected()) {
            encargo = "FUNRURAL";
            contaContabil = "1580";
        } else if (jRbPIS.isSelected()) {
            encargo = "PIS";
            contaContabil = "1581";
        } else if (jRbIRRF.isSelected()) {
            encargo = "IRRF";
            contaContabil = "56699";
        } else if (jRbPensao.isSelected()) {
            encargo = "PENSAO";
            contaContabil = "36104";
        } else if (jRbIRRF1.isSelected()) {
            encargo = "IRRF_T";
            contaContabil = "1563";
        } else if (jRbIRRF2.isSelected()) {
            encargo = "CONTR_T";
            contaContabil = "10841";
        } else {
            encargo = "";
        }
        try {

            if (jCheckBox1.isSelected()) {
                switch (encargo) {
                    case "FGTS":
                        prov.consultaFGTS(competencia);
                        break;

                    case "INSS":
                        prov.consultaINSS(competencia);
                        break;

                    case "FUNRURAL":
                        prov.consultaFUNRURAL(competencia);
                        break;

                    case "PIS":
                        prov.consultaPIS(competencia);

                        break;

                    case "IRRF":
                        prov.consultaIRRF(competencia);

                        break;
                    case "IRRF_T":
                        prov.consultaProvContabil(competencia, encargo, contaContabil);
                        
                        break;
                    case "CONTR_T":
                        prov.consultaProvContabil(competencia, encargo, contaContabil);
                        
                        break;

                    case "PENSAO":

                        break;

                    default:
                        throw new UnsupportedOperationException("Selecione um encargo válido para continuar.");
                }

            }
            prov.consultaContabil(competencia, contaContabil);
            jTextField3.setText(tools.FormatNumbers.numUsToBr(prov.getSaldoContabil()));
            jLabel5.setText("Conta: " + prov.getContaContabil());

            incluiPesquisa(encargo);

            jTextField4.setText(tools.FormatNumbers.numUsToBr(
                    Double.parseDouble(jTextField2.getText().replace(".", "").replace(",", "."))
                    - Double.parseDouble(jTextField3.getText().replace(".", "").replace(",", "."))
            ));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {

            if (jTable1.getRowCount() == 0) {
                throw new Exception();
            }

            if (!jTextField4.getText().equals("0,00")) {
                if (JOptionPane.showConfirmDialog(this, "Há diferença entre a provisão e o Razão Contábil.\nDeseja continuar?", "Integração Financeira", JOptionPane.YES_NO_OPTION) == 1) {
                    throw new Exception();
                }
            }
            //JOptionPane.showMessageDialog(this, "Dados gravados.");
            gravarDados(true);
        } catch (Exception e) {
            System.out.println("Sem dados para integrar com o financeiro.");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

