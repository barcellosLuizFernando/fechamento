    private void inserePesquisa() throws SQLException {
        
        String lctoPrincipal = jTxtLctoPrincipal.getText();
        String parcela = jTxtLctoParcela.getText();
        String lctoBaixa = jTxtLctoBaixa.getText();
        String data = jTxtData.getText();

        String sql;

        switch (var_consulta) {

            case 1:

                jTxtLctoBaixa.setText("");
                jTxtData.setText("");

                sql = "SELECT * FROM desp_antecipada_lancamentos "
                        + "WHERE id = '" + lctoPrincipal + "' "
                        + "AND parcela = '" + parcela + "';";

                cn.iniciarTransacao();
                cn.executeConsulta(sql);
                while (cn.rs.next()) {
                    jTxtLctoBaixa.setText(cn.rs.getString("numero"));
                    jTxtData.setText(dateIn.format(cn.rs.getDate("data")));
                }
                cn.finalizarTransacao();

                break;

            case 2:

                if ("".equals(data)
                        && !"".equals(lctoBaixa)) {
                    sql = "SELECT * FROM ctb_lancamentos "
                            + "WHERE empresa = 1 and numero = '" + lctoBaixa + "';";
                    cnfb.conecta();
                    cnfb.executeConsulta(sql);
                    while (cnfb.rs.next()) {
                        jTxtData.setText(dateIn.format(cnfb.rs.getDate("data_inc")));
                    }
                    cnfb.desconecta();
                }

                break;

        }
    }

    private void limpaCampos() {
        jTxtLctoPrincipal.setText("");
        jTxtLctoBaixa.setText("");
        jTxtLctoParcela.setText("");
        jTxtData.setText("");

        JOptionPane.showMessageDialog(null, "Procedimento executado com sucesso.");

    }

