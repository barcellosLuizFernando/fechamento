    private void incluiPesquisa() {

        DefaultTableModel lista = limpaTabela();
        Double pendente = 0.00;
        Double lancado = 0.00;

        try {

            sql = "SELECT numero, data, docto, serie, frete_pago, placa, "
                    + "case when cod_cooper is null then 'A' else 'T' end as lancado, "
                    + "estab  "
                    + "FROM bsctranscooper.fin_shipping "
                    + "WHERE data BETWEEN '" + tools.ManipulaData.dateUSstr("01/" + jTxtCompetence.getText()) + "' "
                    + "and '" + tools.ManipulaData.dateUSstr(tools.ManipulaData.lastDayToBR(jTxtCompetence.getText())) + "' "
                    + "and fornecedor in (40,55) "
                    + "ORDER BY data, docto;";

            cn.iniciarTransacao();
            cn.executeConsulta(sql);
            while (cn.rs.next()) {

                if (cn.rs.getString("lancado").equals("T")) {
                    lancado += cn.rs.getDouble("frete_pago");
                } else {
                    pendente += cn.rs.getDouble("frete_pago");
                }

                lista.addRow(new Object[]{
                    cn.rs.getString("lancado"),
                    tools.ManipulaData.dateBRstr(cn.rs.getDate("data")),
                    cn.rs.getString("docto"),
                    cn.rs.getString("serie"),
                    cn.rs.getDouble("frete_pago"),
                    cn.rs.getString("placa"),
                    cn.rs.getString("numero"),
                    cn.rs.getString("estab")
                });
            }

        } catch (Exception e) {
            tools.DefaultMsg.errorMsg(e.getMessage());
        } finally {
            cn.finalizarTransacao();
            jTextField1.setText(tools.FormatNumbers.numUsToBr(lancado + pendente));
            jTextField2.setText(tools.FormatNumbers.numUsToBr(pendente));
            jTextField3.setText(tools.FormatNumbers.numUsToBr(lancado));

        }
    }

    private DefaultTableModel limpaTabela() {
        DefaultTableModel lista = (DefaultTableModel) jTable1.getModel();

        lista.setRowCount(0);

        jTable1.setDefaultRenderer(Object.class, new tools.ColorRender());
        jTable1.setDefaultRenderer(Double.class, new tools.ColorRender());

        return lista;
    }

    private void gravaDados() {
        try {
            tools.FinIntegraContasReceber integ;

            int cliente = 0;
            String historico = "";

            for (int i = 0; i < jTable1.getRowCount(); i++) {

                historico = "Frete Cooperado CTe " + jTable1.getValueAt(i, 2).toString() + ", série " + jTable1.getValueAt(i, 3).toString();

                if ("2".equals(jTable1.getValueAt(i, 7))) {
                    cliente = 5371;
                } else {
                    cliente = 4562;
                }

                if (!"T".equals(jTable1.getValueAt(i, 0))) {
                    integ = new FinIntegraContasReceber();
                    integ.setQueryFirst(Short.parseShort("1"),
                            jTable1.getValueAt(i, 2).toString(),
                            historico,
                            tools.ManipulaData.parseBrToDate(jTable1.getValueAt(i, 1).toString()),
                            tools.ManipulaData.vencimentoDiaUtil(jTxtCompetence.getText(), 20, false),
                            1, // ESTABELECIMENTO
                            "S".charAt(0),
                            0, // CONTA CLIENTE
                            cliente, // CLIENTE
                            1, // PORTADOR
                            167, //USUARIO
                            23 // TIPO DOCUMENTO
                    );

                    integ.setQuerySecond(6109, "C", historico, Double.parseDouble(jTable1.getValueAt(i, 4).toString()));

                    cn.iniciarTransacao();

                    integ.syncAccountsPayable();

                    sql = "UPDATE bsctranscooper.fin_shipping SET cod_cooper = " + integ.getCodigo() + " WHERE numero = " + jTable1.getValueAt(i, 6).toString() + ";";

                    cn.executeAtualizacao(sql);
                    cn.finalizarTransacao();

                }

                integ = null;
            }
        } catch (Exception e) {
            tools.DefaultMsg.errorMsg(e.getMessage());
        } finally {
            incluiPesquisa();
            tools.DefaultMsg.saveDataSuccessfull();
        }

    }
