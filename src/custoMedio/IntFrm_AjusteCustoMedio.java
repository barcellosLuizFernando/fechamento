    private DefaultTableModel montaTabela() {
        DefaultTableModel lista = (DefaultTableModel) jTable1.getModel();

        lista.setRowCount(0);
        lista.setColumnCount(0);

        lista.addColumn("Produto");
        lista.addColumn("Filial");
        lista.addColumn("Data");
        lista.addColumn("Valor");

        return lista;
    }

    private void incluiPesquisa() {
        DefaultTableModel lista = montaTabela();
        dti = jTxtDataInicial.getText().replace("/", ".");
        dtf = jTxtDataFinal.getText().replace("/", ".");

        String sql = "select ent.empresa, ent.codigo, ent.dt_entrada, ent.estabelec,"
                + "ent.fornecedor, prod.produto, prod.sequencia, prod.custo_item, "
                + "prod.unitario, p.nome as nome_produto "
                + "from com_entradas ent "
                + "inner join com_entradas_produtos prod on "
                + "     (prod.empresa = ent.empresa and "
                + "     prod.codigo = ent.codigo and "
                + "     prod.operacao = 6833 and "
                + "     round(prod.unitario,2) = round(prod.custo_item,2)) "
                + "left join cad_produtos p on (p.empresa = prod.empresa and p.codigo = prod.produto) "
                + "where ent.empresa = 1 and ent.dt_entrada between '" + dti + "' and '" + dtf + "'"
                + "order by p.nome,ent.dt_entrada;";

        if (cnfb.conecta()) {
            try {
                cnfb.executeConsulta(sql);
                while (cnfb.rs.next()) {
                    lista.addRow(new String[]{
                        cnfb.rs.getString("nome_produto"),
                        cnfb.rs.getString("estabelec"),
                        dateIn.format(cnfb.rs.getDate("dt_entrada")),
                        df.format(cnfb.rs.getDouble("custo_item"))
                    });
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            } finally {
                cnfb.desconecta();
            }
        }

        if (jTable1.getRowCount() > 0) {
            jButton2.setEnabled(true);
        } else {
            jButton2.setEnabled(false);
        }

    }
