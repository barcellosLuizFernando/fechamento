    private void somaSaldos() {

        Double valorAcumulado = 0.00;

        for (int i = 0; i < jTable1.getRowCount(); i++) {

            valorAcumulado += Double.parseDouble(jTable1.getValueAt(i, 4).toString().replace(".", "").replace(",", "."));

            jTextField2.setText("" + (i + 1));
        }

        jTextField1.setText(df.format(valorAcumulado));
    }

