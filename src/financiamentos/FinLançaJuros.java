    private void buscaContratos(String competencia) {

        DefaultTableModel lista = montaTabela();

        if (cn.iniciarTransacao()) {
            try {
                sql = " call fechamento.fin_lancamentos('" + dateOut.format(mData.lastDay(competencia)) + "')";
                cn.executeAtualizacao(sql);
                sql = "SELECT * FROM tmp_lancamentos WHERE (lancamento = 0 or lancamento is null) and juros > 0.00;";
                cn.executeConsulta(sql);
                while (cn.rs.next()) {
                    lista.addRow(new String[]{
                        cn.rs.getString("numero"),
                        cn.rs.getString("fornecedor"),
                        dateIn.format(cn.rs.getDate("data")),
                        dateIn.format(cn.rs.getDate("vencimento")),
                        df.format(cn.rs.getDouble("juros")),
                        cn.rs.getString("conta"),
                        cn.rs.getString("tipodoc"),
                        "Valor juros sobre contrato " + cn.rs.getString("numero")
                        + " " + cn.rs.getString("banco"),
                        cn.rs.getString("id")
                    });

                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            } finally {
                cn.finalizarTransacao();
            }
        }

    }

