    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = jTextField1.getText();
        String descricao = jTextField2.getText();
        String obs = jTextField3.getText();

        if ("".equals(id)) {

            sql = "INSERT INTO fechamento.fin_modalidades (descricao, obs) "
                    + "VALUES ('" + descricao + "','" + obs + "')";

        } else {
            sql = "UPDATE fechamento.fin_modalidades "
                    + "SET descricao = '" + descricao + "', obs = '" + obs + "' "
                    + "WHERE id = '" + id + "';";
        }

        if (cn.iniciarTransacao()) {
            try {
                if (cn.executeAtualizacao(sql)) {
                    limpaCampos();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            } finally {
                cn.finalizarTransacao();
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

