    private void incluiPesquisa() {

        int linha = jTblConsulta_Multi.getSelectedRow();

        switch (var_consulta) {
            case 1:

                jTextField1.setText(jTblConsulta_Multi.getValueAt(linha, 0).toString());
                jTextField2.setText(jTblConsulta_Multi.getValueAt(linha, 1).toString());
                jTextField3.setText(jTblConsulta_Multi.getValueAt(linha, 2).toString());

                break;

            case 2:

                sql = "SELECT * "
                        + "FROM fechamento.fin_modalidades "
                        + "WHERE id = '" + jTextField1.getText() + "';";

                if (cn.iniciarTransacao()) {
                    try {
                        cn.executeConsulta(sql);
                        while (cn.rs.next()) {
                            jTextField1.setText(cn.rs.getString("id"));
                            jTextField2.setText(cn.rs.getString("descricao"));
                            jTextField3.setText(cn.rs.getString("obs"));
                        }
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e.getMessage());
                    } finally {
                        cn.finalizarTransacao();
                    }
                }

                break;
        }

    }

    private void montaLista() {

        DefaultTableModel lista = (DefaultTableModel) jTblConsulta_Multi.getModel();

        lista.setRowCount(0);
        lista.setColumnCount(0);

        lista.addColumn("Id");
        lista.addColumn("Descrição");
        lista.addColumn("Observação");

        sql = "SELECT * "
                + "FROM fechamento.fin_modalidades "
                + "ORDER BY descricao;";

        if (cn.iniciarTransacao()) {
            try {

                cn.executeConsulta(sql);
                while (cn.rs.next()) {
                    lista.addRow(new String[]{
                        cn.rs.getString("id"),
                        cn.rs.getString("descricao"),
                        cn.rs.getString("obs")
                    });
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            } finally {
                cn.finalizarTransacao();
            }
        }

    }

    private void limpaCampos() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
    }
