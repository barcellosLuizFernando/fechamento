    private void montaTabelaContratos() {
        DefaultTableModel lista = (DefaultTableModel) jTable2.getModel();
        lista.setRowCount(0);
        jTable2.setDefaultRenderer(Object.class, new tools.ColorRender());

        try {
            sql = "SELECT "
                    + "min(c.id) as id, "
                    + "c.numero, "
                    + "b.title as banco "
                    + "FROM fechamento.fin_contratos c "
                    + "left join fechamento.cad_bancos b on b.id = c.banco "
                    + "group by c.numero "
                    + "order by b.title, c.numero;";
            cn.iniciarTransacao();
            cn.executeConsulta(sql);
            while (cn.rs.next()) {
                lista.addRow(new Object[]{
                    cn.rs.getString("id"),
                    cn.rs.getString("numero"),
                    cn.rs.getString("banco")
                });
            }

        } catch (Exception e) {
            tools.DefaultMsg.errorMsg(e.getMessage());
        } finally {
            cn.finalizarTransacao();
        }

    }

    private void saveData() {

        try {
            if (var_gravar) {
                sql = ("INSERT INTO fechamento.fin_contratos_garantias (id, id_bem, "
                        + "grau, local_registro, dt_baixa, quantidade, bx_autom) VALUES ( '"
                        + jTable2.getValueAt(jTable2.getSelectedRow(), 0).toString() + "','"
                        + jTxtIdBem.getText() + "','" + jTxtGrau.getText() + "','"
                        + jTxtLocalReg.getText() + "','"
                        + tools.ManipulaData.dateUSstr(jTxtDtBx.getText()) + "','"
                        + jTxtQtd.getText().replace(".", "").replace(",", ".") + "',"
                        + jCheckBox1.isSelected() + ");").replace("'null'", "null");
            } else {
                sql = ("UPDATE fechamento.fin_contratos_garantias SET "
                        + "grau = '" + jTxtGrau.getText() + "',"
                        + "local_registro = '" + jTxtLocalReg.getText() + "',"
                        + "dt_baixa = '" + tools.ManipulaData.dateUSstr(jTxtDtBx.getText()) + "',"
                        + "quantidade = '" + jTxtQtd.getText().replace(".", "").replace(",", ".") + "',"
                        + "bx_autom = " + jCheckBox1.isSelected() + " "
                        + "where id = '" + jTable2.getValueAt(jTable2.getSelectedRow(), 0).toString() + "' and "
                        + "id_bem = '" + jTxtIdBem.getText() + "';").replace("'null'", "null");

            }
            cn.iniciarTransacao();
            cn.executeAtualizacao(sql);

            tools.ClearFields.ClearFields(jPanel2);

            var_gravar = true;

        } catch (Exception e) {
            tools.DefaultMsg.errorMsg(e.getMessage());
        } finally {
            cn.finalizarTransacao();
            montaTabelaGarantias();
        }
    }

    private void montaTabelaGarantias() {

        var_gravar = true;

        jTxtIdBem.setEnabled(true);

        DefaultTableModel lista = (DefaultTableModel) jTable1.getModel();
        lista.setRowCount(0);
        jTable1.setDefaultRenderer(Object.class, new tools.ColorRender());

        try {
            sql = "select g.*, b.nome "
                    + "from fechamento.fin_contratos_garantias g "
                    + "left join fechamento.cad_bens b on b.id = g.id_bem "
                    + "where g.id = '" + jTable2.getValueAt(jTable2.getSelectedRow(), 0).toString() + "';";
            cn.iniciarTransacao();
            cn.executeConsulta(sql);
            while (cn.rs.next()) {
                lista.addRow(new Object[]{
                    cn.rs.getString("id_bem"),
                    cn.rs.getString("nome"),
                    cn.rs.getString("grau"),
                    cn.rs.getString("local_registro"),
                    cn.rs.getBoolean("bx_autom"),
                    tools.ManipulaData.dateBRstr(cn.rs.getDate("dt_baixa")),
                    tools.FormatNumbers.numUsToBr(cn.rs.getDouble("quantidade"))
                });
            }
        } catch (Exception e) {
            tools.DefaultMsg.errorMsg(e.getMessage());
        } finally {
            cn.finalizarTransacao();
        }
    }

    private void mostraDadosGarantias() {

        jTxtIdBem.setEnabled(false);

        int linha = jTable1.getSelectedRow();

        jTxtIdBem.setText(jTable1.getValueAt(linha, 0).toString());
        jTxtNomeBem.setText(jTable1.getValueAt(linha, 1).toString());
        jTxtGrau.setText(jTable1.getValueAt(linha, 2).toString());
        jTxtLocalReg.setText(jTable1.getValueAt(linha, 3).toString());
        jTxtQtd.setText(jTable1.getValueAt(linha, 6).toString());
        try {
            jCheckBox1.setSelected(Boolean.parseBoolean(jTable1.getValueAt(linha, 4).toString()));

            tools.ManipulaData.parseBrToDate(jTable1.getValueAt(linha, 5).toString());

            jTxtDtBx.setText(jTable1.getValueAt(linha, 5).toString());
        } catch (Exception e) {
            jTxtDtBx.setText("");
        }

        var_gravar = false;
    }

    private void incluiPesquisa() {
        switch (var_consulta) {
            case 1:
                try {
                    sql = "select * from fechamento.cad_bens "
                            + "where id = '" + jTxtIdBem.getText() + "';";
                    cn.iniciarTransacao();
                    cn.executeConsulta(sql);
                    while (cn.rs.next()) {
                        jTxtNomeBem.setText(cn.rs.getString("nome"));
                    }
                } catch (Exception e) {
                    jTxtNomeBem.setText("");
                } finally {
                    cn.finalizarTransacao();
                }
                break;
        }
    }

    private void atualizaPelaSeta(JTable tbl, KeyEvent evt) {
        if (evt.getKeyCode() == KeyEvent.VK_UP
                | evt.getKeyCode() == KeyEvent.VK_DOWN) {

            if (tbl.equals(jTable1)) {
                mostraDadosGarantias();
            } else {
                montaTabelaGarantias();
            }

        }
    }
